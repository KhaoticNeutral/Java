public class CustomerT {
    private String name;
    private String ssn;

    public CustomerT(String name, String ssn) {
        this.name = name;
        this.ssn = ssn;
    }

    public String getName() {
        return name;
    }

    public void setName(String n) {
        name = n;
    }

    public String getSSN() {
        return ssn;

    }

}
